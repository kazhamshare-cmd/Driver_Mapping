// Locale管理用Notifier
class LocaleNotifier extends StateNotifier<Locale> {
  LocaleNotifier() : super(const Locale('ja')) {
    _loadLocale();
  }

  static const _prefsKey = 'selected_locale';

  Future<void> _loadLocale() async {
    final prefs = await SharedPreferences.getInstance();
    final langCode = prefs.getString(_prefsKey);
    if (langCode != null) {
      state = Locale(langCode);
    }
  }

  Future<void> setLocale(AppLocale appLocale) async {
    final langCode = appLocale.name; // 'ja', 'en', 'th'
    state = Locale(langCode);
    final prefs = await SharedPreferences.getInstance();
    await prefs.setString(_prefsKey, langCode);
  }
}

// Provider定義
final localeProvider = StateNotifierProvider<LocaleNotifier, Locale>((ref) {
  return LocaleNotifier();
});

// 翻訳アクセス用Provider
final translationProvider = Provider<AppTranslations>((ref) {
  final locale = ref.watch(localeProvider);
  AppLocale appLocale;
  switch (locale.languageCode) {
    case 'en':
      appLocale = AppLocale.en;
      break;
    case 'th':
      appLocale = AppLocale.th;
      break;
    default:
      appLocale = AppLocale.ja;
  }
  return AppTranslations(appLocale);
});
