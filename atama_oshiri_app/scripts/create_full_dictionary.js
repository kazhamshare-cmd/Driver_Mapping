// 単語を分類して整理
function categorizeWords(words) {
  const categories = {
    'あ': [], 'い': [], 'う': [], 'え': [], 'お': [],
    'か': [], 'き': [], 'く': [], 'け': [], 'こ': [],
    'さ': [], 'し': [], 'す': [], 'せ': [], 'そ': [],
    'た': [], 'ち': [], 'つ': [], 'て': [], 'と': [],
    'な': [], 'に': [], 'ぬ': [], 'ね': [], 'の': [],
    'は': [], 'ひ': [], 'ふ': [], 'へ': [], 'ほ': [],
    'ま': [], 'み': [], 'む': [], 'め': [], 'も': [],
    'や': [], 'ゆ': [], 'よ': [],
    'ら': [], 'り': [], 'る': [], 'れ': [], 'ろ': [],
    'わ': [], 'を': [], 'ん': []
  };

  words.forEach(word => {
    if (word.length >= 2) {
      const firstChar = word[0];
      if (categories[firstChar]) {
        categories[firstChar].push(word);
      }
    }
  });

  return categories;
}

// メイン処理
function main() {
  console.log('全文字の現実的な日本語辞書を作成中...');
  
  const categories = categorizeWords(allWords);
  
  let totalWords = 0;
  
  // 各カテゴリの単語数を表示
  Object.keys(categories).forEach(category => {
    if (categories[category].length > 0) {
      console.log(`「${category}」で始まる: ${categories[category].length}個`);
      totalWords += categories[category].length;
    }
  });
  
  console.log(`\n総単語数: ${totalWords}個`);
  console.log(`重複除去後の総単語数: ${[...new Set(allWords)].length}個`);
  
  // 各文字の辞書ファイルを作成
  Object.keys(categories).forEach(category => {
    if (categories[category].length > 0) {
      const uniqueWords = [...new Set(categories[category])];
      const outputPath = path.join(__dirname, `../assets/dictionary_new/char_${category}.json`);
      fs.writeFileSync(outputPath, JSON.stringify(uniqueWords, null, 2), 'utf8');
      console.log(`「${category}」の辞書を ${outputPath} に保存しました。`);
    }
  });
}

main();
